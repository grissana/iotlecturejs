// operator ตัวดำเนินการ Ep.1
// คือ เครื่องหมายที่ใช้ในการดำเนินการกับข้อมูล
//------------------------------
// Arithmetic Operators + - * / % ++ -- **
console.log(10 ** 5)

// Comparison Operators == === != !== > < >= <=
// ค่าที่ return จะเป็น boolean true หรือ false 1 หรือ 0
console.log('10' == 10) // true ไม่สนใจ type
console.log('10' === 10) // false สนใจ type
console.log('Sombat' >= 'Somjai')
console.log('Thailand' < 'Thaipia')
// Logical Operators &&  ||  !
//เอา true กับ true มากระทำกันผลที่ได้จะมี true กับ false 
console.log(true && true)
console.log(true && false)
console.log(true || false)
console.log(!true)
console.log(!false)
console.log('---------------------------------')
// Bitwise Operators & | ^ ~ << >> >>>
// การทำงานเป็น bit คือ 0 หรือ 1
console.log(5 & 1)
console.log(5 | 1)
console.log(5 ^ 1)
console.log(~5)
console.log(5 << 1)
console.log(5 >> 1)
console.log(5 >>> 1)


