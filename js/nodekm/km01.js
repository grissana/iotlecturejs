// ชนิดข้อมูล
// 1. ชนิดข้อมูลข้อความ (String)
console.log('hello from km01.js');
// 2. ชนิดข้อมูลตัวเลข (Number)
console.log(10+20);
// 3. ชนิดข้อมูลจํานวนเต็ม (Integer)
console.log(10+20);
// 4. ชนิดข้อมูลจํานวนจริง (Float)
console.log(10.5+20.5);
// 5. ชนิดข้อมูลจํานวนเต็มและจํานวนจริง (Float)
console.log(10+20.5);
// 6. ชนิดข้อมูลตรรกะ (Boolean)
console.log(true);
// 7. ชนิดข้อมูลวัตถุ (Object)
console.log({name:'john',age:20});
// 8. ชนิดข้อมูลอาร์เรย์ (Array)
console.log([10,20,30]);
// 9. ชนิดข้อมูลฟังก์ชัน (Function)
console.log(function(){});
// 10. ชนิดข้อมูลวัตถุที่ไม่มีค่า (Null)
console.log(null);
// 11. ชนิดข้อมูลที่ไม่ได้กําหนดค่า (Undefined)
console.log(undefined);
// 12. ชนิดข้อมูลที่เกิดจากการคัดลอกข้อมูล (Symbol)
console.log(Symbol('foo'));
// 13. ชนิดข้อมูลที่เกิดจากการคัดลอกข้อมูล (BigInt)
console.log(10n);
// 14. ชนิดข้อมูลที่เกิดจากการคัดลอกข้อมูล (Map)
console.log(new Map());
// 15. ชนิดข้อมูลที่เกิดจากการคัดลอกข้อมูล (Set)
console.log(new Set());
// 16. ชนิดข้อมูลที่เกิดจากการคัดลอกข้อมูล (WeakMap)
console.log(new WeakMap());
// 17. ชนิดข้อมูลที่เกิดจากการคัดลอกข้อมูล (WeakSet)
console.log(new WeakSet());
// 18. ชนิดข้อมูลที่เกิดจากการคัดลอกข้อมูล (Date)
console.log(new Date());
// 19. ชนิดข้อมูลที่เกิดจากการคัดลอกข้อมูล (RegExp)
console.log(new RegExp());
